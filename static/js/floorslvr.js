var oneflr = function(strt, end) {
    //reading in proper floor 
    flr = floors[strt[0]];
    lns = []; //creating list that will have coords for line directons
    //making 1st coord
    lns[lns.length] = [flr[strt][4], flr[strt][5]];
    //making 2nd coord (and if needed 2.5th coord)
    if ((!("AL" in flr)) ||
	((Math.abs(parseInt(flr[strt][4]) - parseInt(flr["CL"][0]))) <
	 (Math.abs(parseInt(flr[strt][4]) - parseInt(flr["AL"][0]))))) {
	// if there's no auxiliary line or the room is closer to the center line than the auxiliary line
	lns[lns.length] = [flr["CL"][0] , flr[strt][5]];
    }
    
    else {
	lns[lns.length] = [flr["AL"][0] , flr[strt][5]];
	if ( Math.abs(flr[end][5] - flr["AL"][1]) < Math.abs(flr[end][5] - flr["AL"][3])){
	    lns[lns.length] = [flr["AL"][0] , flr["AL"][1]];
	    lns[lns.length] = [flr["CL"][0] , flr["AL"][1]];
	}
	
	else {
            lns[lns.length] = [flr["AL"][0] , flr["AL"][3]];
	    lns[lns.length] = [flr["CL"][0] , flr["AL"][3]];
     	}
    }   
    //making 3rd coord		
    lns[lns.length] = [flr["CL"][0] , flr[end][5]];
    
    //making 4th coord
    lns[lns.length] = [flr[end][4] , flr[end][5]];

    return lns;
    

}

var multiflr = function(strt, end) {
    fs = parseInt(strt[0]);
    ff = parseInt(end[0]);
    flr = floors[strt[0]];
    retarr = [];
    if (Math.abs(flr[strt[5]]-flr[(fs+"WS")[5]]) < Math.abs(flr[strt[5]]-flr[(fs+"ES")[5]])){
		if (Math.abs(flr[strt[5]]-flr[(fs+"WS")[5]]) < Math.abs(flr[strt][5]-flr[fs+"EL"][5])){
			retarr = retarr + oneflr(strt, (fs+"WS")) + oneflr((ff+"WS"), end);
		}
		if (Math.abs(flr[strt][5]-flr[fs+"WS"][5]) > Math.abs(flr[strt][5]-flr[fs+"EL"][5])){
			retarr = retarr + oneflr(strt, (fs+"EL")) + oneflr((ff+"EL"), end);
		}
    }
    else{
		retarr = retarr + oneflr(strt, (fs+"ES")) + oneflr((ff+"ES"), end);
    }
    return retarr;
};


